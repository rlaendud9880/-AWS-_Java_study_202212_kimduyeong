package j01_출력;

public class Output2 {

	public static void main(String[] args) {
		//System.out.println(); -> sysout + ctrl + space + enter : 빠르게 입력 가능
		
		int year = 1;
		
		System.out.println("이름 : 김준일"); // ctrl + alt 누르고 영역 설정 후 화살표 아래로 하면 그대로 복사 가능하다.
		System.out.println("나이 : " + (20 + year));
		System.out.println("이름 : 김준이"); // alt + 좌클릭 : 영역 선택해서 다중입력 가능
		System.out.println("나이 : " + (21 + year));
		System.out.println("이름 : 김준삼"); 
		System.out.println("나이 : " + (22 + year));
		System.out.println("이름 : 김준사"); 
		System.out.println("나이 : " + (23 + year));

	}

}
